/**
  * @brief This function handles USART2 global interrupt.
  */


void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
	data.lastByte = USART2->DR;
	if(data.lastByte == '\r' || data.lastByte == '\n'){
		//se envia el queue
		xQueueSendFromISR(xQueue, &data, NULL);
		memset(&data, 0, data.i);					//reinicia la memoria

		//recibir un queue
		if(xQueueReceiveFromISR(xQueueAdc,&adc_data, NULL)){
			//DATO RECIBIDO
			SEGGER_SYSVIEW_PrintfHost("ADC->%d\n\r",adc_data.ch_value);
		}
	}else{
		data.data[data.i] = data.lastByte;
		data.i += 1;
	}
  /* USER CODE END USART2_IRQn 0 */
  HAL_UART_IRQHandler(&huart2);
  /* USER CODE BEGIN USART2_IRQn 1 */

  /* USER CODE END USART2_IRQn 1 */
}

